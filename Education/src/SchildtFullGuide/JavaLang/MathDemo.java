package SchildtFullGuide.JavaLang;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println("sin (1.5 * pi * R) = " + Math.sin(Math.PI * 1.5));
        System.out.println("cos (1 * pi * R) = " + Math.cos(Math.PI * 1));
        System.out.println("tan (0.5 * pi * R) = " + Math.tan(Math.PI * 0.5));

        System.out.println("asin (-1) = " + Math.asin(-1.0));
        System.out.println("acos (-1) = " + Math.acos(-1.0));
        System.out.println("atan (-1) = " + Math.atan(-1.0));

        // ну там еще всякие логарифмы есть и т.п.

    }
}
